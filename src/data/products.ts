import { generateProductImages } from "@/lib/product-images";

/** Загрузить JSON fallback при недоступности БД */
function loadJsonFallback<T>(file: string): T | null {
  try {
    const p = path.join(process.cwd(), "data", file);
    if (!existsSync(p)) return null;
    return JSON.parse(readFileSync(p, "utf-8")) as T;
  } catch {
    return null;
  }
}

export interface MarketplaceLink {
  name: "Wildberries" | "Ozon" | "Yandex Market";
  url: string;
  icon: string;
}

export interface Product {
  id: string;
  slug: string;
  name: string;
  price: number;
  originalPrice: number;
  currency: string;
  category: string;
  description: string;
  image: string;
  images: string[];
  marketplaces: MarketplaceLink[];
  wbArticle: number;
  ozonArticle?: number;
  rating?: number;
  reviewsCount?: number;
  salesCount?: number;
  imtId?: number;
  colorName?: string;
  siblingIds?: number[];
  composition?: string;
  nameAutoGenerated?: boolean;
  descAutoGenerated?: boolean;
  archivedAt?: string;
  wbCreatedAt?: string;
  characteristics?: CharacteristicGroup[];
  photoCount: number;
}

export interface CharacteristicOption {
  name: string;
  value: string;
  charc_type?: number;
}

export interface CharacteristicGroup {
  group_name: string;
  options: CharacteristicOption[];
}

export interface ProductCategory {
  slug: string;
  name: string;
  description: string;
  image: string;
  count: number;
}

const CATEGORY_INFO: Record<string, { name: string; description: string }> = {
  crossbody: { name: "Кросс-боди", description: "Сумки через плечо" },
  "na-plecho": { name: "На плечо", description: "Сумки на плечо" },
  baguette: { name: "Багет", description: "Сумки-багет" },
  tote: { name: "Тоут", description: "Шоперы и тоуты" },
  saddle: { name: "Седло", description: "Сумки-седло" },
  backpack: { name: "Рюкзак", description: "Рюкзаки" },
};

function mapProduct(p: PrismaProduct): Product {
  const marketplaces = (p.marketplaces ?? []) as unknown as MarketplaceLink[];

  // Generate image URLs from wbArticle + photoCount (single source of truth)
  const photoCount = p.photoCount || p.images?.length || 1;
  const computed = generateProductImages(p.wbArticle, photoCount);
  const image = computed?.image || p.image || "";
  const images = computed?.images?.length ? computed.images : p.images;

  return {
    id: p.id,
    slug: p.slug,
    name: p.name,
    price: p.price,
    originalPrice: p.originalPrice,
    currency: p.currency,
    category: p.category,
    description: p.description,
    image,
    images,
    marketplaces,
    wbArticle: p.wbArticle ?? 0,
    ozonArticle: p.ozonArticle ?? undefined,
    rating: p.rating ?? undefined,
    reviewsCount: p.reviewsCount ?? undefined,
    salesCount: p.salesCount ?? undefined,
    imtId: p.imtId ?? undefined,
    colorName: p.colorName ?? undefined,
    siblingIds: p.siblingIds,
    composition: p.composition ?? undefined,
    nameAutoGenerated: p.nameAutoGenerated ?? undefined,
    descAutoGenerated: p.descAutoGenerated ?? undefined,
    archivedAt: p.archivedAt?.toISOString() ?? undefined,
    wbCreatedAt: p.wbCreatedAt?.toISOString() ?? undefined,
    characteristics: p.characteristics
      ? (p.characteristics as unknown as CharacteristicGroup[])
      : undefined,
    photoCount,
  };
}

export async function getProducts(): Promise<Product[]> {
  return cacheGet("all-products", async () => {
    try {
      const rows = await prismaQuery(() =>
        prisma.product.findMany({ orderBy: { createdAt: "asc" } }),
      );
      return rows.filter((p) => !p.archivedAt).map(mapProduct);
    } catch (err) {
      logger.warn("DB unavailable, fallback to products.json", {
        error: (err as Error)?.message,
      });
      const fallback = loadJsonFallback<{ products: Product[] }>("products.json");
      if (!fallback?.products) throw err;
      return fallback.products;
    }
  });
}

export async function getProduct(slug: string): Promise<Product | null> {
  // ——— Сначала ищем в кеше неархивных продуктов ———
  const all = await getProducts();
  const found = all.find((p) => p.slug === slug);
  if (found) return found;

  // ——— Архивный товар? Прямой запрос к БД + JSON fallback ———
  return cacheGet(`product:${slug}`, async () => {
    try {
      const row = await prismaQuery(() =>
        prisma.product.findUnique({ where: { slug } }),
      );
      if (row) return mapProduct(row);
    } catch {
      // DB cold-start — fallback ниже
    }
    const fallback = loadJsonFallback<{ products: Product[] }>("products.json");
    return fallback?.products?.find((p) => p.slug === slug) ?? null;
  });
}

export async function getProductsByCategory(
  category: string,
): Promise<Product[]> {
  const rows = await prismaQuery(() =>
    prisma.product.findMany({
      where: { category, archivedAt: null },
      orderBy: { createdAt: "asc" },
    }),
  );
  return rows.map(mapProduct);
}

export async function getProductsByWbArticle(
  article: number,
): Promise<Product | null> {
  const row = await prismaQuery(() =>
    prisma.product.findUnique({ where: { wbArticle: article } }),
  );
  return row ? mapProduct(row) : null;
}

export async function getCategories(): Promise<ProductCategory[]> {
  return cacheGet("all-categories", async () => {
    try {
      const counts = await prismaQuery(() =>
        prisma.product.groupBy({
          by: ["category"],
          where: { archivedAt: null },
          _count: { id: true },
        }),
      );

      const countMap = new Map(counts.map((c) => [c.category, c._count.id]));

      return Object.entries(CATEGORY_INFO).map(([slug, info]) => ({
        slug,
        name: info.name,
        description: info.description,
        image: `/images/categories/${slug}.jpg`,
        count: countMap.get(slug) ?? 0,
      }));
    } catch (err) {
      logger.warn("DB unavailable, fallback to products.json for categories", {
        error: (err as Error)?.message,
      });
      const fallback = loadJsonFallback<{
        categories: ProductCategory[];
      }>("products.json");
      if (!fallback?.categories) throw err;
      return fallback.categories;
    }
  });
}

export async function getAllSlugs(): Promise<string[]> {
  try {
    const rows = await prismaQuery(() =>
      prisma.product.findMany({
        where: { archivedAt: null },
        select: { slug: true },
      }),
    );
    return rows.map((r) => r.slug);
  } catch (err) {
    logger.warn("DB unavailable, fallback to products.json for slugs", {
      error: (err as Error)?.message,
    });
    const fallback = loadJsonFallback<{ products: { slug: string }[] }>(
      "products.json",
    );
    if (!fallback?.products) throw err;
    return fallback.products.map((p) => p.slug);
  }
}

#!/usr/bin/env node
/**
 * Ежедневный бэкап БД → data/products.json
 * 
 * Читает данные из PostgreSQL через Prisma и обновляет JSON fallback.
 * Запускать раз в сутки (cron / Task Scheduler).
 * 
 * Запуск: node scripts/backup-fallback.mjs
 */

import { PrismaClient } from '@prisma/client';
import { PrismaPg } from '@prisma/adapter-pg';
import { readFileSync, writeFileSync } from 'fs';

function log(msg) {
  const ts = new Date().toISOString().replace('T', ' ').substring(0, 19);
  console.log(`[${ts}] ${msg}`);
}

// ——— 1. DATABASE_URL ———
if (!process.env.DATABASE_URL) {
  try {
    const env = readFileSync('.env.local', 'utf8');
    const m = env.match(/DATABASE_URL="([^"]+)"/);
    if (m) process.env.DATABASE_URL = m[1];
  } catch { /* ignore */ }
}
if (!process.env.DATABASE_URL) {
  log('❌ DATABASE_URL не найден');
  process.exit(1);
}

const prisma = new PrismaClient({
  adapter: new PrismaPg({ connectionString: process.env.DATABASE_URL || "" }),
});

// ——— 2. Retry-обёртка ———
async function retry(fn) {
  for (let i = 1; i <= 3; i++) {
    try { return await fn(); }
    catch (e) {
      log(`⚠️  попытка ${i}: ${(e.message || '').substring(0, 80)}`);
      if (i < 3) await new Promise(r => setTimeout(r, 3000));
    }
  }
  throw new Error('Не удалось после 3 попыток');
}

// ——— 3. Категории ———
const CATEGORY_INFO = {
  crossbody: { name: 'Кросс-боди', description: 'Сумки через плечо' },
  'na-plecho': { name: 'На плечо', description: 'Сумки на плечо' },
  baguette: { name: 'Багет', description: 'Сумки-багет' },
  tote: { name: 'Тоут', description: 'Шоперы и тоуты' },
  saddle: { name: 'Седло', description: 'Сумки-седло' },
  backpack: { name: 'Рюкзак', description: 'Рюкзаки' },
};

// ——— 4. Main ———
async function main() {
  log('🔄 Запуск бэкапа...');

  // Прогрев
  await retry(() => prisma.$queryRawUnsafe('SELECT 1'));

  // Продукты
  const products = await retry(() =>
    prisma.product.findMany({
      where: { archivedAt: null, inStock: true },
      orderBy: { id: 'asc' },
    })
  );

  // Маппинг в формат приложения
  const mapped = products.map(p => ({
    id: p.id,
    slug: p.slug,
    sku: p.sku || undefined,
    name: p.name,
    price: p.price,
    originalPrice: p.originalPrice,
    wbPrice: p.wbPrice || undefined,
    wbOriginalPrice: p.wbOriginalPrice || undefined,
    ozonPrice: p.ozonPrice || undefined,
    ozonOriginalPrice: p.ozonOriginalPrice || undefined,
    currency: p.currency,
    category: p.category,
    description: p.description,
    image: p.image || '',
    images: p.images || [],
    marketplaces: (() => {
      const mps = Array.isArray(p.marketplaces) ? p.marketplaces : [];
      const result = [];
      const wbArt = p.wbArticle ? Number(p.wbArticle) : null;
      const ozonArt = p.ozonArticle ? Number(p.ozonArticle) : null;
      if (wbArt && !mps.some(m => m.name === 'Wildberries')) {
        result.push({
          name: 'Wildberries',
          url: `https://www.wildberries.ru/catalog/${wbArt}/detail.aspx`,
          icon: '/images/icons/wb.svg',
        });
      }
      if (ozonArt && !mps.some(m => m.name === 'Ozon')) {
        result.push({
          name: 'Ozon',
          url: `https://www.ozon.ru/product/${ozonArt}/`,
          icon: '/images/icons/ozon.svg',
        });
      }
      for (const mp of mps) {
        if (!result.some(m => m.name === mp.name)) result.push(mp);
      }
      return result;
    })(),
    wbArticle: p.wbArticle ? Number(p.wbArticle) : 0,
    ozonArticle: p.ozonArticle ? Number(p.ozonArticle) : undefined,
    rating: p.rating || undefined,
    reviewsCount: p.reviewsCount || undefined,
    salesCount: p.salesCount || undefined,
    colorName: p.colorName || undefined,
    composition: p.composition || undefined,
    nameAutoGenerated: p.nameAutoGenerated || undefined,
    descAutoGenerated: p.descAutoGenerated || undefined,
    archivedAt: p.archivedAt?.toISOString() || undefined,
    inStock: p.inStock,
    wbCreatedAt: p.wbCreatedAt?.toISOString() || undefined,
    characteristics: p.characteristics || undefined,
    photoCount: p.photoCount || 1,
    modelId: p.modelId || undefined,
  }));

  // Категории
  const catCounts = {};
  for (const p of products) catCounts[p.category] = (catCounts[p.category] || 0) + 1;
  const categories = Object.entries(CATEGORY_INFO).map(([slug, info]) => ({
    slug,
    name: info.name,
    description: info.description,
    image: `/images/categories/${slug}.jpg`,
    count: catCounts[slug] || 0,
  }));

  // Settings
  let settings = null;
  try {
    const s = await retry(() => prisma.settings.findFirst());
    if (s) settings = s.data;
  } catch { /* settings не обязательны */ }

  // Запись
  const output = { products: mapped, categories, settings };
  const json = JSON.stringify(output, (k, v) => typeof v === 'bigint' ? Number(v) : v, 2);
  writeFileSync('data/products.json', json);
  const sizeKB = (Buffer.byteLength(json) / 1024).toFixed(1);

  log(`✅ Бэкап готов: ${mapped.length} товаров, ${categories.length} категорий, ${sizeKB} KB`);
}

main().catch(e => {
  log(`❌ Ошибка: ${e.message || e}`);
  process.exit(1);
}).finally(() => prisma.$disconnect());

import { readFileSync, writeFileSync } from 'fs';

const raw = JSON.parse(readFileSync('backup-moranti.json', 'utf8'));

const CATEGORY_INFO = {
  crossbody: { name: 'Кросс-боди', description: 'Сумки через плечо' },
  'na-plecho': { name: 'На плечо', description: 'Сумки на плечо' },
  baguette: { name: 'Багет', description: 'Сумки-багет' },
  tote: { name: 'Тоут', description: 'Шоперы и тоуты' },
  saddle: { name: 'Седло', description: 'Сумки-седло' },
  backpack: { name: 'Рюкзак', description: 'Рюкзаки' },
};

// Map raw Prisma product → app Product (same logic as src/data/products.ts)
function mapProduct(p) {
  const mps = Array.isArray(p.marketplaces) ? p.marketplaces : [];
  const marketplaces = [];
  if (p.wbArticle && !mps.some(m => m.name === 'Wildberries')) {
    marketplaces.push({
      name: 'Wildberries',
      url: `https://www.wildberries.ru/catalog/${p.wbArticle}/detail.aspx`,
      icon: '/images/icons/wb.svg',
    });
  }
  if (p.ozonArticle && !mps.some(m => m.name === 'Ozon')) {
    marketplaces.push({
      name: 'Ozon',
      url: `https://www.ozon.ru/product/${p.ozonArticle}/`,
      icon: '/images/icons/ozon.svg',
    });
  }
  for (const mp of mps) {
    if (!marketplaces.some(m => m.name === mp.name)) {
      marketplaces.push(mp);
    }
  }

  const photoCount = p.photoCount || (p.images?.length) || 1;

  return {
    id: p.id,
    slug: p.slug,
    sku: p.sku || undefined,
    name: p.name,
    price: p.price,
    originalPrice: p.originalPrice,
    wbPrice: p.wbPrice || undefined,
    wbOriginalPrice: p.wbOriginalPrice || undefined,
    ozonPrice: p.ozonPrice || undefined,
    ozonOriginalPrice: p.ozonOriginalPrice || undefined,
    currency: p.currency,
    category: p.category,
    description: p.description,
    image: p.image || '',
    images: p.images || [],
    marketplaces,
    wbArticle: p.wbArticle || 0,
    ozonArticle: p.ozonArticle || undefined,
    rating: p.rating || undefined,
    reviewsCount: p.reviewsCount || undefined,
    salesCount: p.salesCount || undefined,
    colorName: p.colorName || undefined,
    composition: p.composition || undefined,
    nameAutoGenerated: p.nameAutoGenerated || undefined,
    descAutoGenerated: p.descAutoGenerated || undefined,
    archivedAt: p.archivedAt || undefined,
    inStock: p.inStock,
    wbCreatedAt: p.wbCreatedAt || undefined,
    characteristics: p.characteristics || undefined,
    photoCount,
    modelId: p.modelId || undefined,
  };
}

// Products: only active, not archived
const active = raw.products.filter(p => !p.archivedAt && p.inStock);
const products = active.map(mapProduct);

// Categories: group active products by category, count
const counts = {};
for (const p of active) {
  counts[p.category] = (counts[p.category] || 0) + 1;
}
const categories = Object.entries(CATEGORY_INFO).map(([slug, info]) => ({
  slug,
  name: info.name,
  description: info.description,
  image: `/images/categories/${slug}.jpg`,
  count: counts[slug] || 0,
}));

// Settings from backup (raw Prisma data → data field)
const settings = raw.settings?.data || null;

const output = { products, categories, settings };
writeFileSync('data/products.json', JSON.stringify(output, null, 2));
console.log(`✅ data/products.json updated`);
console.log(`   Products: ${products.length}`);
console.log(`   Categories: ${categories.length}`);
console.log(`   Settings: ${settings ? 'yes' : 'no'}`);
console.log(`   Size: ${(Buffer.byteLength(JSON.stringify(output)) / 1024).toFixed(1)} KB`);

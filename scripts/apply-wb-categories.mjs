/**
 * apply-wb-categories.mjs — Обновляет категории товаров по данным WB
 *
 * Берет категории из WB filter API (5 моделей сумок)
 * + мои предложения для товаров без модели на WB
 * + перегенерирует названия через name-generator
 */

import { readFileSync } from "fs";
import { PrismaClient } from "@prisma/client";
import { createRequire } from "module";

const require = createRequire(import.meta.url);
const { CATEGORY_RU } = require("./wb-categories");
const { generateName } = require("./name-generator");

// Категории напрямую из WB filter API (/__internal/u-catalog/brands/v8/filters)
const wbCategoryMap = {};
for (const [cat, articles] of Object.entries({
  crossbody: [
    226025376, 253053689, 577159834, 185673251, 391176745,
    584837180, 969189657, 253051367, 584839195, 165436665,
    445414843, 226024975, 899375103, 391198267, 969260081,
    969257682, 584838455, 226023435, 969265618,
  ],
  "na-plecho": [969025312, 969020680],
  baguette: [
    584856106, 462984217, 253033047, 584857819,
    165436666, 391159701, 969178673,
  ],
  tote: [
    391171927, 584878143, 584881064, 899355983,
    584864653, 328035699, 584882297, 328031735, 463001457,
  ],
  saddle: [261294903, 462993756, 226020314],
})) {
  for (const a of articles) wbCategoryMap[a] = cat;
}

// Мои предложения для товаров без модели на WB
const proposals = {
  462990511: "baguette", // mor-029, features: 'Багет'
  391150373: "baguette", // mor-031, features: 'Багет'
  462991980: "baguette", // mor-045, features: 'Багет'
  445415947: "tote", // mor-010, shopper
  119833702: "tote", // mor-013, shopper
  462987423: "tote", // mor-027, shopper
  144391368: "tote", // mor-037, shopper
  969008238: "crossbody", // mor-038, 13x23cm, через плечо
  899303821: "backpack", // mor-014
  899321020: "backpack", // mor-018
  899312868: "backpack", // mor-049
  899315968: "backpack", // mor-051
};

const fullMap = { ...wbCategoryMap, ...proposals };

// Загружаем WB данные для характеристик
const wbData = JSON.parse(readFileSync("data/wb-scrape-result.json", "utf-8"));
const wbById = {};
for (const p of wbData.products) wbById[p.article] = p;

const prisma = new PrismaClient();

async function main() {
  const products = await prisma.product.findMany({
    where: { archivedAt: null },
    orderBy: { id: "asc" },
  });

  let updated = 0;
  let skipped = 0;

  for (const p of products) {
    const cat = fullMap[p.wbArticle];
    if (!cat) {
      console.log(`⚠  ${p.id} (арт.${p.wbArticle}) — НЕТ КАТЕГОРИИ, пропускаем`);
      skipped++;
      continue;
    }

    // Генерируем новое название
    const wbInfo = wbById[p.wbArticle];
    const composition =
      wbInfo?.characteristics?.find((c) => c.name === "Состав")?.value || "";
    const wbName = wbInfo?.name || "";
    const newName = generateName({ category: cat, composition, wbName });

    // Обновляем
    await prisma.product.update({
      where: { id: p.id },
      data: {
        category: cat,
        name: newName,
        nameAutoGenerated: true,
      },
    });

    console.log(
      `✅ ${p.id} | ${cat.padEnd(10)} | ${p.name.padEnd(40)} → ${newName}`
    );
    updated++;
  }

  console.log(
    `\nГотово: ${updated} обновлено, ${skipped} пропущено (без категории)`
  );

  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});

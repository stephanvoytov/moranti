/* =============================================
   Moranti — Product Data (54 товара, Wildberries)
   Источник: Neon Postgres (через Prisma)
   Fallback: data/products.json (если БД пуста/недоступна)
   ============================================= */

import { readFileSync } from "fs";
import path from "path";
import prisma from "@/lib/prisma";

export interface Product {
  id: string;
  slug: string;
  name: string;
  price: number;
  originalPrice: number;
  currency: string;
  category: string;
  description: string;
  image: string;
  images: string[];
  marketplaces: MarketplaceLink[];
  wbArticle: number;
  ozonArticle?: number;
  rating?: number;
  reviewsCount?: number;
  salesCount?: number;
  imtId?: number;
  colorName?: string;
  siblingIds?: number[];
  composition?: string;
  /** Флаг: имя сгенерировано скриптом (если false — ручное, не перезаписывать) */
  nameAutoGenerated?: boolean;
  /** Флаг: описание сгенерировано скриптом */
  descAutoGenerated?: boolean;
  /** Дата архивации (если товар скрыт из WB) */
  archivedAt?: string;
}

export interface MarketplaceLink {
  name: "Wildberries" | "Ozon" | "Yandex Market";
  url: string;
  icon: string;
}

export interface ProductCategory {
  slug: string;
  name: string;
  description: string;
  image: string;
  count: number;
}

/* =============================================
   Fallback: читаем JSON-файл
   ============================================= */

interface ProductsFile {
  products: Product[];
  categories: ProductCategory[];
}

function readJsonFallback(): ProductsFile {
  try {
    const filePath = path.join(process.cwd(), "data", "products.json");
    return JSON.parse(readFileSync(filePath, "utf-8"));
  } catch {
    return { products: [], categories: [] };
  }
}

let fallbackCache: ProductsFile | null = null;

function getFallback(): ProductsFile {
  if (!fallbackCache) fallbackCache = readJsonFallback();
  return fallbackCache;
}

/* =============================================
   Prisma → Product конвертер
   ============================================= */

function toProduct(p: Awaited<ReturnType<typeof prisma.product.findFirst>>): Product | null {
  if (!p) return null;
  return {
    id: p.id,
    slug: p.slug,
    name: p.name,
    price: p.price,
    originalPrice: p.originalPrice,
    currency: p.currency,
    category: p.category,
    description: p.description,
    image: p.image,
    images: p.images,
    marketplaces: (p.marketplaces ?? []) as unknown as MarketplaceLink[],
    wbArticle: p.wbArticle ?? 0,
    ozonArticle: p.ozonArticle ?? undefined,
    rating: p.rating ?? undefined,
    reviewsCount: p.reviewsCount ?? undefined,
    salesCount: p.salesCount ?? undefined,
    imtId: p.imtId ?? undefined,
    colorName: p.colorName ?? undefined,
    siblingIds: p.siblingIds && p.siblingIds.length > 0 ? p.siblingIds : undefined,
    composition: p.composition ?? undefined,
    nameAutoGenerated: p.nameAutoGenerated ?? undefined,
    descAutoGenerated: p.descAutoGenerated ?? undefined,
    archivedAt: p.archivedAt?.toISOString() ?? undefined,
  };
}

/* =============================================
   Query functions (async — Prisma + fallback)
   ============================================= */

export function invalidateCache(): void {
  fallbackCache = null;
}

export async function getProducts(): Promise<Product[]> {
  try {
    const rows = await prisma.product.findMany({
      orderBy: { createdAt: "asc" },
    });
    if (rows.length > 0) {
      return rows.map((p) => toProduct(p)!);
    }
  } catch {
    // Prisma недоступна — fallback
  }
  return getFallback().products;
}

export async function getProduct(slug: string): Promise<Product | null> {
  try {
    const p = await prisma.product.findUnique({ where: { slug } });
    if (p) return toProduct(p);
  } catch {
    // fallback
  }
  return getFallback().products.find((p) => p.slug === slug) ?? null;
}

export async function getProductsByCategory(category: string): Promise<Product[]> {
  try {
    const rows = await prisma.product.findMany({
      where: { category, archivedAt: null },
      orderBy: { createdAt: "asc" },
    });
    if (rows.length > 0) return rows.map((p) => toProduct(p)!);
  } catch {
    // fallback
  }
  return getFallback().products.filter((p) => p.category === category);
}

export async function getProductsByWbArticle(article: number): Promise<Product | null> {
  try {
    const p = await prisma.product.findUnique({
      where: { wbArticle: article },
    });
    if (p) return toProduct(p);
  } catch {
    // fallback
  }
  return getFallback().products.find((p) => p.wbArticle === article) ?? null;
}

export async function getCategories(): Promise<ProductCategory[]> {
  try {
    const rows = await prisma.product.findMany({
      where: { archivedAt: null },
      select: { category: true },
    });
    if (rows.length > 0) {
      const counts: Record<string, number> = {};
      for (const r of rows) {
        counts[r.category] = (counts[r.category] || 0) + 1;
      }
      // Берём описание категорий из fallback (там есть description + image)
      const fallback = getFallback();
      return fallback.categories.map((cat) => ({
        ...cat,
        count: counts[cat.slug] || 0,
      }));
    }
  } catch {
    // fallback
  }
  return getFallback().categories;
}

/** Для generateStaticParams — ищем slug'и */
export async function getAllSlugs(): Promise<string[]> {
  try {
    const rows = await prisma.product.findMany({
      select: { slug: true },
    });
    if (rows.length > 0) return rows.map((r) => r.slug);
  } catch {
    // fallback
  }
  return getFallback().products.map((p) => p.slug);
}

/** preload для кэширования данных (замена старой invalidateProductsCache) */
export async function preloadProducts(): Promise<Product[]> {
  return getProducts();
}

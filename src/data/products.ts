  slug: string;
  name: string;
  price: number;
  originalPrice: number;
  wbPrice?: number;
  wbOriginalPrice?: number;
  ozonPrice?: number;
  ozonOriginalPrice?: number;
  currency: string;
  category: string;
  description: string;
  image: string;
  images: string[];
  marketplaces: MarketplaceLink[];
  wbArticle: number;
